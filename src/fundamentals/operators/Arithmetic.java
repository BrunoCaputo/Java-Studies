package fundamentals.operators;

public class Arithmetic {

    public static void main(String[] args) {
        double x = 32523.35;
        double y = 1355.234;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        int a = 4132;
        int b = 1231;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a / (double) b);

        System.out.println(a % b);
    }
}
