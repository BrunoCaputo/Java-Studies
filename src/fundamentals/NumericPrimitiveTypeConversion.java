package fundamentals;

public class NumericPrimitiveTypeConversion {

    public static void main(String[] args) {
        // CASTING
        float a = (float) 1.0;
        System.out.println(a);

        int b = 4;
        byte c = (byte) b;
        System.out.println(c);

        double d = 1.999999;
        int e = (int) d;
        System.out.println(e);
    }
}
